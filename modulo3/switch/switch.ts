let flor = "tulipa";

switch (flor) {
    case 'rosa':
        console.log("Rosas sao vermelhas")
        break;
    case 'violeta':
        console.log("Violetas sao azuis")
        break;
    case 'tulipa':
        console.log("Tulipas sao vermelhas")
        break;
    default:
        console.log("Por favor, selecione uma outra flor")
        break;
}